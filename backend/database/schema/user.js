const mongoose = require('mongoose');

export const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        required: true,
    },
    auth: {
        type: Number,
        required: true,
    },
})